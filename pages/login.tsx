export async function getServerSideProps({ req }: GetServerSidePropsContext) {
  const providers = await getProviders();
  const session = await getSession({ req });

  if (session) {
    return {
      redirect: {
        destination: "/", // or any other page you want to redirect to
        permanent: false,
      },
    };
  }

  return {
    props: {
      providers,
    },
  };
}
